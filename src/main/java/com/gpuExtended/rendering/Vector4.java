package com.gpuExtended.util.rendering;

public class Vector4
{
    public double x;
    public double y;
    public double z;
    public double w;

    public Vector4(double x, double y, double z, double w)
    {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    }
}
